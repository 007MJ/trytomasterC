#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;
	int	j;

	i = 0;
	j = 0;
	n = 3;
	while (i < n)
	{
		i++;
		while (j < i)
		{
			j++;
			printf("#");
		}
		printf("\n");
	}
}
